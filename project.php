				<section id="introduction">
					<div><h2>Introduction of protein</h2></div>
					<div>
						<h3>Overview</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;Proteins are large biomolecules and macromolecules that are comprised of one or more long chains of amino acid residues. Proteins perform a vast array of functions within organisms, including catalysing metabolic reactions, DNA replication, responding to stimuli, providing structure to cells and organisms, and transporting molecules from one location to another. Proteins differ from one another primarily in their sequence of amino acids, which is dictated by the nucleotide sequence of their genes, and which usually results in protein folding into a specific 3D structure that determines its activity. 
						</span>
						<h3>Composition</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;A linear chain of amino acid residues is called a polypeptide. A protein contains at least one long polypeptide. Short polypeptides, containing less than 20–30 residues, are rarely considered to be proteins and are commonly called peptides, or sometimes oligopeptides. The individual amino acid residues are bonded together by peptide bonds and adjacent amino acid residues. The sequence of amino acid residues in a protein is defined by the sequence of a gene, which is encoded in the genetic code. In general, the genetic code specifies 20 standard amino acids; but in certain organisms the genetic code can include selenocysteine and—in certain archaea—pyrrolysine. Shortly after or even during synthesis, the residues in a protein are often chemically modified by post-translational modification, which alters the physical and chemical properties, folding, stability, activity, and ultimately, the function of the proteins. Some proteins have non-peptide groups attached, which can be called prosthetic groups or cofactors. Proteins can also work together to achieve a particular function, and they often associate to form stable protein complexes.
						</span>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;Once formed, proteins only exist for a certain period and are then degraded and recycled by the cell's machinery through the process of protein turnover. A protein's lifespan is measured in terms of its half-life and covers a wide range. They can exist for minutes or years with an average lifespan of 1–2 days in mammalian cells. Abnormal or misfolded proteins are degraded more rapidly either due to being targeted for destruction or due to being unstable.
						</span>
						<h3>Function</h3>
						<span class="section-content">
							 &nbsp;&nbsp;&nbsp;&nbsp;Like other biological macromolecules such as polysaccharides and nucleic acids, proteins are essential parts of organisms and participate in virtually every process within cells. Many proteins are enzymes that catalyse biochemical reactions and are vital to metabolism. Proteins also have structural or mechanical functions, such as actin and myosin in muscle and the proteins in the cytoskeleton, which form a system of scaffolding that maintains cell shape. Other proteins are important in cell signaling, immune responses, cell adhesion, and the cell cycle. In animals, proteins are needed in the diet to provide the essential amino acids that cannot be synthesized. Digestion breaks the proteins down for use in the metabolism.<br>
						</span>
					</div>
				</section>
				<section id="structure">
					<div><h2>Protein structure</h2></div>
					<div>
						<h3>Overview</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;Most proteins fold into unique 3D structures. The shape into which a protein naturally folds is known as its native conformation. Although many proteins can fold unassisted, simply through the chemical properties of their amino acids, others require the aid of molecular chaperones to fold into their native states. Biochemists often refer to four distinct aspects of a protein's structure: primary, secondary, tertiary and quaternary structure.
							<img src="pic/structure_overview.png" alt="Not found" style="width: 30vw; display: block; margin: 1em auto; padding: 0; border: 1.5px solid black;">
						</span>
						<h3>Primary structure</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;The simplest level of protein structure, primary structure, is simply the sequence of amino acids in a polypeptide chain. For example, the hormone insulin has two polypeptide chains, A and B. (The insulin molecule shown here is cow insulin, although its structure is similar to that of human insulin.) Each chain has its own set of amino acids, assembled in a particular order. For instance, the sequence of the A chain starts with glycine at the N-terminus and ends with asparagine at the C-terminus, and is different from the sequence of the B chain. 
							<img src="pic/primary_structure.jpg" alt="Not found" style="width: 30vw; display: block; margin: 1em auto; padding: 0; border: 1.5px solid black;">
						</span>
						<h3>Secondary structure</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;The next level of protein structure, secondary structure, refers to local folded structures that form within a polypeptide due to interactions between atoms of the backbone. (The backbone just refers to the polypeptide chain apart from the R groups – so all we mean here is that secondary structure does not involve R group atoms.) The most common types of secondary structures are the α helix and the β pleated sheet. Both structures are held in shape by hydrogen bonds, which form between the carbonyl O of one amino acid and the amino H of another.<br>
							&nbsp;&nbsp;&nbsp;&nbsp;In an α helix, the carbonyl (C=O) of one amino acid is hydrogen bonded to the amino H (N-H) of an amino acid that is four down the chain. (E.g., the carbonyl of amino acid 1 would form a hydrogen bond to the N-H of amino acid 5.) This pattern of bonding pulls the polypeptide chain into a helical structure that resembles a curled ribbon, with each turn of the helix containing 3.6 amino acids. The R groups of the amino acids stick outward from the α helix, where they are free to interact.<br>
							&nbsp;&nbsp;&nbsp;&nbsp;In a β pleated sheet, two or more segments of a polypeptide chain line up next to each other, forming a sheet-like structure held together by hydrogen bonds. The hydrogen bonds form between carbonyl and amino groups of backbone, while the R groups extend above and below the plane of the sheet. The strands of a β pleated sheet may be parallel, pointing in the same direction (meaning that their N- and C-termini match up), or antiparallel, pointing in opposite directions (meaning that the N-terminus of one strand is positioned next to the C-terminus of the other).
							<img src="pic/secondary_structure.png" alt="Not found" style="width: 30vw;display: block; margin: 1em auto; padding: 0;">
						</span>
						<h3>Tertiary structure</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;The overall three-dimensional structure of a polypeptide is called its tertiary structure. The tertiary structure is primarily due to interactions between the R groups of the amino acids that make up the protein.<br>
							&nbsp;&nbsp;&nbsp;&nbsp;R group interactions that contribute to tertiary structure include hydrogen bonding, ionic bonding, dipole-dipole interactions, and London dispersion forces – basically, the whole gamut of non-covalent bonds. For example, R groups with like charges repel one another, while those with opposite charges can form an ionic bond. Similarly, polar R groups can form hydrogen bonds and other dipole-dipole interactions. Also important to tertiary structure are hydrophobic interactions, in which amino acids with nonpolar, hydrophobic R groups cluster together on the inside of the protein, leaving hydrophilic amino acids on the outside to interact with surrounding water molecules.<br>
							&nbsp;&nbsp;&nbsp;&nbsp;Finally, there’s one special type of covalent bond that can contribute to tertiary structure: the disulfide bond. Disulfide bonds, covalent linkages between the sulfur-containing side chains of cysteines, are much stronger than the other types of bonds that contribute to tertiary structure. They act like molecular "safety pins," keeping parts of the polypeptide firmly attached to one another.
							<img src="pic/tertiary_structure.png" width=60% alt="Not found" style="display: block; margin: 1em auto; padding: 0; border: 1.5px solid black;">
						</span>
						<h3>Quaternary structure</h3>
						<span class="section-content">
							&nbsp;&nbsp;&nbsp;&nbsp;Many proteins are made up of a single polypeptide chain and have only three levels of structure (the ones we’ve just discussed). However, some proteins are made up of multiple polypeptide chains, also known as subunits. When these subunits come together, they give the protein its quaternary structure.<br>
							&nbsp;&nbsp;&nbsp;&nbsp;We’ve already encountered one example of a protein with quaternary structure: hemoglobin. As mentioned earlier, hemoglobin carries oxygen in the blood and is made up of four subunits, two each of the α and β types. Another example is DNA polymerase, an enzyme that synthesizes new strands of DNA and is composed of ten subunits.<br>
							&nbsp;&nbsp;&nbsp;&nbsp;In general, the same types of interactions that contribute to tertiary structure (mostly weak interactions, such as hydrogen bonding and London dispersion forces) also hold the subunits together to give quaternary structure.
							<img src="pic/quaternary_structure.jpg" alt="Not found" style="width: 30vw;display: block; margin: 1em auto; padding: 0; border: 1.5px solid black;">
						</span>	
